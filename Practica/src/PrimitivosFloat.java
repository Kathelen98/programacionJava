public class PrimitivosFloat {
    public static void main(String[] args) {

        float realFloat = 1;
        System.out.printf("realFloat = " + realFloat );

        float realFloat1 = 1.5e4f;
        System.out.printf("realFloat1 = " + realFloat1);

        float realFloat2 = 1.5e-4f;
        System.out.printf("realFloat2 =" + realFloat2);
    }
}
